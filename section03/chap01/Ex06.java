package section03.chap01;
// 복합 대입 연산자 추가 예제
public class Ex06 {
    public static void main(String[] args) {
        int x = 3;                     // x = 3
        x += 2;                        // x = 5
        x -= 3;                        // x = 2
        x *= 12;                       // x = 24
        x /= 3;                        // x = 8
        x %= 5;                        // x = 3
    }
}
